    private void jSaveButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSaveButton1ActionPerformed
        // TODO add your handling code here:
       SubTask st = new SubTask(jTitleTextField1.getText());
        task.addSubTask(st);
        //JOptionPane.showMessageDialog(null,"List Successfully Added");
        int result =JOptionPane.showConfirmDialog (null, "SubTask Created \n Would you like to add a Note?","Confirmation",JOptionPane.YES_NO_OPTION);
        if (result== JOptionPane.YES_OPTION){
            new CreateNoteGUI(st).setVisible(true);
            dispose();
        }
        else{
            int result1 =JOptionPane.showConfirmDialog (null, " Would you like to add an other SubTask?",null,JOptionPane.YES_NO_OPTION);
        if (result1== JOptionPane.YES_OPTION){
            new CreateSubTaskGUI(task).setVisible(true);
            dispose();
        }
        else{
            new ViewTasksGUI(task).setVisible(true);
            dispose();
        }
        }
        
        
    }//GEN-LAST:event_jSaveButton1ActionPerformed

