    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            Items item = user.returnItems(Integer.parseInt(jTextField1.getText()));
        }
        catch (Exception e) {     
		System.out.println(e.toString()); 
                JOptionPane.showMessageDialog(null, "Must use an existing ID",null,JOptionPane.WARNING_MESSAGE);
                
         }
       Items item = user.returnItems(Integer.parseInt(jTextField1.getText()));
       if(item instanceof List){
           new ListGUI((List)item).setVisible(true);
       }
       else if(item instanceof Note){
           new ViewNoteGUI((Note)item).setVisible(true);
       }
       else{
           JOptionPane.showMessageDialog(null, "Item does not exist",null,JOptionPane.WARNING_MESSAGE);
       }
       dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

