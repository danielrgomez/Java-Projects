    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int itemID =Integer.parseInt(JOptionPane.showInputDialog("Which Item would you like ot share?"));
        Items item= user.returnItems(itemID);
        item.updateSharing();
        String uEmail  =JOptionPane.showInputDialog("What is your firend's email?");
        User u = GuiltList.searchUsers(uEmail);
        u.addToItemList(item);
        JOptionPane.showMessageDialog(null, "Don't share too much \nFriends make great for enemies",null,JOptionPane.WARNING_MESSAGE);
        
    }//GEN-LAST:event_jButton2ActionPerformed

